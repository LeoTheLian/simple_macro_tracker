# Database
# https://docs.djangoproject.com/en/6.0/ref/settings/#databases

# Database - parsed from DATABASE_URL env var
# Format: postgres://USER:PASSWORD@HOST:PORT/DBNAME  or  sqlite:///db.sqlite3
_db_url = os.environ.get('DATABASE_URL', '')

if _db_url.startswith('postgres'):
    import urllib.parse as _up
    _p = _up.urlparse(_db_url)
    DATABASES = {
        'default': {
            'ENGINE': 'django.db.backends.postgresql',
            'NAME': _p.path.lstrip('/'),
            'USER': _p.username,
            'PASSWORD': _p.password,
            'HOST': _p.hostname,
            'PORT': str(_p.port or 5432),
        }
    }
elif _db_url.startswith('sqlite'):
    # sqlite:///path/to/db.sqlite3  (relative to BASE_DIR)
    _sqlite_path = _db_url.replace('sqlite:///', '')
    DATABASES = {
        'default': {
            'ENGINE': 'django.db.backends.sqlite3',
            'NAME': BASE_DIR / _sqlite_path,
        }
    }
else:
    DATABASES = {
        'default': {
            'ENGINE': 'django.db.backends.sqlite3',
            'NAME': BASE_DIR / 'db.sqlite3',
        }
    }


